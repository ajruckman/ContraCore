// +build ignore

package main

import (
    "bufio"
    "fmt"
    "io/ioutil"
    "os"
)

func main() {
    fmt.Println("Generating run.go")

    err := os.Chdir("./provision")
    if err != nil {
        panic(err)
    }

    if _, err = os.Stat("./run.go"); err == nil {
        err = os.Remove("./run.go")
        if err != nil {
            panic(err)
        }
    }

    file, err := os.Create("run.go")
    if err != nil {
        panic(err)
    }

    w := bufio.NewWriter(file)

    ddlsql, err := ioutil.ReadFile("./sql/10_ddl.sql")
    if err != nil {
        panic(err)
    }

    w.WriteString("package provision\n\n")
    w.WriteString("// Code generated by generate.go; DO NOT EDIT.\n\n")
    w.WriteString("import `github.com/coredns/coredns/plugin/pkg/log`\n")
    w.WriteString("import `github.com/ajruckman/ContraCore/internal/db/contradb`\n")
    w.WriteString("import `github.com/ajruckman/ContraCore/internal/system`\n\n")
    w.WriteString("func Setup() {\n")
    w.WriteString("    clog := log.NewWithPlugin(`ContraCore`)\n")
    w.WriteString("    if !system.ContraDBOnline.Load() {\n")
    w.WriteString("        clog.Warning(`ContraDB is not connected; not provisioning database`)\n")
    w.WriteString("        return\n")
    w.WriteString("    }\n")
    w.WriteString("    clog.Info(`Provisioning database`)\n")
    w.WriteString("    _, err := contradb.Exec(`\n")
    w.WriteString(string(ddlsql))
    w.WriteString("    `)\n")
    w.WriteString("    if err != nil { panic(err) }\n")
    w.WriteString("}")

    w.Flush()
    file.Close()
}
